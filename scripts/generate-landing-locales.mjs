import { readFile, writeFile } from 'node:fs/promises';
import vm from 'node:vm';

const targets = {
  ar: 'ar', ca: 'ca', zh: 'zh-CN', da: 'da', nl: 'nl', fi: 'fi', fr: 'fr', de: 'de',
  el: 'el', he: 'he', hi: 'hi', hu: 'hu', id: 'id', it: 'it', ja: 'ja', ko: 'ko',
  no: 'no', pl: 'pl', pt: 'pt', ro: 'ro', ru: 'ru', es: 'es', sv: 'sv', th: 'th',
  tr: 'tr', uk: 'uk', vi: 'vi',
};

const source = await readFile(new URL('../i18n.js', import.meta.url), 'utf8');
const sandbox = {
  window: { DuoLandingLocales: {} },
  localStorage: { getItem: () => null, setItem: () => {} },
  location: { search: '', origin: 'https://duocards.xyz', pathname: '/' },
  navigator: { language: 'en' },
  document: { addEventListener: () => {}, getElementById: () => null, querySelector: () => null, querySelectorAll: () => [], createElement: () => ({ dataset: {} }), head: { appendChild: () => {} }, dispatchEvent: () => {} },
  CustomEvent: class {}, URLSearchParams, Set,
};
vm.createContext(sandbox);
vm.runInContext(source, sandbox);

const english = sandbox.window.DuoI18n.T.en;
const templates = {
  _cardAriaTpl: 'Card {w} — Enter flips, arrows sort into piles',
  _cardPluralTpl: '{n} cards',
  _demoDoneLiveTpl: 'Demo complete. Learned {done} of {total} cards.',
  _demoResultTpl: 'Nice work. You know {done} of {total}.',
  _msgNoTpl: '{w} → Don\'t know pile, back to the bottom of the deck.',
  _msgYesTpl: '{w} → Know pile.',
  _msgYesDoneTpl: '{w} — third time known, moved to Learned.',
  _msgDoneTpl: '{w} → Learned pile.',
  _msgQueueTpl: '{msg} In deck: {n} cards.',
};
const sourcePack = {
  ...english,
  demoTitle: 'Swipe cards. Flip cards. Learn words.',
  aiTitle: 'One tap. A finished flashcard.',
  chromeTitle: 'Watch videos. Words are saved automatically.',
  commTitle: 'Thousands of flashcard decks from people learning the same language as you.',
  commBody: 'Millions of community flashcards are waiting. Or build your own flashcard deck in one minute.',
  statsTitle: 'Your learning score keeps growing.',
  flipNote: 'Free. No credit card. Start learning in one minute.',
  ...templates,
};
const entries = [];
for (const [key, value] of Object.entries(sourcePack)) {
  if (typeof value === 'string') entries.push({ key, index: null, text: value });
  if (Array.isArray(value)) value.forEach((text, index) => entries.push({ key, index, text }));
}

const separator = '__DUOCARDS_SEPARATOR_9F3A__';
const protectedValues = ['DuoCards', 'app.duocards.xyz', 'duocards.xyz', '&nbsp;', '{w}', '{n}', '{done}', '{total}', '{msg}'];
const protect = text => {
  let result = text;
  protectedValues.forEach((value, index) => { result = result.split(value).join(`__DUO_KEEP_${index}__`); });
  return result;
};
const restore = text => {
  let result = text;
  protectedValues.forEach((value, index) => { result = result.split(`__DUO_KEEP_${index}__`).join(value); });
  return result;
};

const chunks = [];
let chunk = [];
let length = 0;
for (const entry of entries) {
  const text = protect(entry.text);
  if (chunk.length && length + text.length > 3200) { chunks.push(chunk); chunk = []; length = 0; }
  chunk.push({ ...entry, text });
  length += text.length + separator.length + 2;
}
if (chunk.length) chunks.push(chunk);

const requestTranslation = async (q, target) => {
  const url = new URL('https://translate.googleapis.com/translate_a/single');
  url.searchParams.set('client', 'gtx');
  url.searchParams.set('sl', 'en');
  url.searchParams.set('tl', target);
  url.searchParams.set('dt', 't');
  url.searchParams.set('q', q);
  const response = await fetch(url);
  if (!response.ok) throw new Error(`Translation failed (${target}): ${response.status}`);
  const data = await response.json();
  return data[0].map(part => part[0]).join('');
};

const translateChunk = async (items, target) => {
  const q = items.map(item => item.text).join(`\n${separator}\n`);
  const translated = await requestTranslation(q, target);
  const parts = translated.split(separator).map(part => restore(part.trim()));
  if (parts.length !== items.length) {
    console.log(`Batch fallback for ${target}`);
    return Promise.all(items.map(async item => restore((await requestTranslation(item.text, target)).trim())));
  }
  return parts;
};

const packs = {};
for (const [code, target] of Object.entries(targets)) {
  const pack = {};
  for (const items of chunks) {
    const translated = await translateChunk(items, target);
    items.forEach((entry, i) => {
      if (entry.index == null) pack[entry.key] = translated[i];
      else {
        pack[entry.key] ||= [];
        pack[entry.key][entry.index] = translated[i];
      }
    });
  }
  packs[code] = pack;
  console.log(`Translated ${code}`);
}

const output = `/* Generated by scripts/generate-landing-locales.mjs. */\nwindow.DuoLandingLocales = ${JSON.stringify(packs, null, 2)};\n`;
await writeFile(new URL('../landing-locales.js', import.meta.url), output, 'utf8');
